     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(CompanyUpdateRequest $request, $id)
    {
        $requestData = $request->safe()->only(['name', 'email', 'address']);
        $updateCompany = Company::findOrFail($id)->update($requestData);

        return response()->json($updateCompany, 200);
    }

